									->setCategory("Report Generator Document");
		
		$objPHPExcel->setActiveSheetIndex(0);		
		//logo
		$company=$this->session->userdata('param_company');
		$logo=$this->session->userdata('logo');
		$title='Laporan Pendapatan ZIS '.$rsCab->KOTA.' Bulan '.$blnStr[$bln]."  ".$thn;
		$namafile="pendapatanZis_".$rsCab->KOTA.$thn.$bln;

		$objDrawing = new PHPExcel_Worksheet_Drawing();
		$objDrawing->setName('Logo');
		$objDrawing->setDescription('Logo');
		$objDrawing->setPath('assets/img/'.$logo);	//**
		$objDrawing->setHeight(70);
		$objDrawing->setCoordinates('A1');
		$objDrawing->setOffsetX(0);		
		$objDrawing->setWorksheet($objPHPExcel->getActiveSheet());
		 
		// Header
		$objPHPExcel->getActiveSheet()->mergeCells('C1:AF1');
		$objPHPExcel->getActiveSheet()->setCellValue('C1', $company);
		$objPHPExcel->getActiveSheet()->getStyle('C1')->getFont()->setSize(20);
		$objPHPExcel->getActiveSheet()->getStyle('C1')->getFont()->setBold(true);
		
		$objPHPExcel->getActiveSheet()->mergeCells('C2:AF2');
		$objPHPExcel->getActiveSheet()->setCellValue('C2', $title);
		$objPHPExcel->getActiveSheet()->getStyle('C2')->getFont()->setSize(14);
		$objPHPExcel->getActiveSheet()->getStyle('C2')->getFont()->setBold(true);
		
		$objPHPExcel->getActiveSheet()->mergeCells('C3:AF3');
		$objPHPExcel->getActiveSheet()->setCellValue('C3', 'Diterbitkan Tgl :'.date('d F Y'));
		$objPHPExcel->getActiveSheet()->getStyle('C3')->getFont()->setSize(12);
		
		$objPHPExcel->getActiveSheet()->getStyle('C3')->getFont()->setSize(12);
		$setHeaderRow=array(
					'font'    => array(
						'bold'      => true
					),
					'alignment' => array(
						'horizontal' => PHPExcel_Style_Alignment::HORIZONTAL_CENTER,
					),
					'borders' => array(
						'allborders'     => array(
							'style' => PHPExcel_Style_Border::BORDER_THIN,
							'color' => array('argb' => '000000')
						)				
					),
					'fill' => array(
						'type'       => PHPExcel_Style_Fill::FILL_GRADIENT_LINEAR,
						'rotation'   => 90,
						'startcolor' => array(
							'argb' => 'FFA0A0A0'
						),
						'endcolor'   => array(
							'argb' => 'FFFFFFFF'
						)
					)
				);

		$objPHPExcel->getActiveSheet()->setTitle('realisasi'.$thn.$bln);
		//header table

		//isi

		$row=5;
		$objPHPExcel->getActiveSheet()->setCellValue('B'.$row, 'NAMA CABANG '.strtoupper($rsCab->KOTA));
		$objPHPExcel->getActiveSheet()->getStyle('B'.$row)->getFont()->setSize(14);
		$objPHPExcel->getActiveSheet()->getStyle('B'.$row)->getFont()->setBold(true);
		$row++;
		$objPHPExcel->getActiveSheet()->setCellValue('B'.$row, 'PERIODE '.$blnStr[$bln]."  ".$thn);
		$objPHPExcel->getActiveSheet()->getStyle('B'.$row)->getFont()->setSize(14);
		$objPHPExcel->getActiveSheet()->getStyle('B'.$row)->getFont()->setBold(true);
		
		$row+=2;
		$str="select * from ak_rekeningperkiraan WHERE id_cab=".$cab_pilih." and status=1 and idacc like '4.%' and level=5 order by nama";
		$rsZIS=$this->db->query($str)->result();

		$objPHPExcel->getActiveSheet()->setCellValue('B'.$row, 'Pendapatan ')->getStyle('B'.$row)->getFont()->setSize(14);
		$objPHPExcel->getActiveSheet()->getStyle('B'.$row)->getFont()->setBold(true);
		$row++;
		$objPHPExcel->getActiveSheet()->setCellValue('C'.$row, 'Pendapatan Dana')->getStyle('B'.$row)->getFont()->setSize(14);
		$objPHPExcel->getActiveSheet()->getStyle('C'.$row)->getFont()->setBold(true);
		$row++;

		if (sizeof ($rsZIS)<=0){
			$objPHPExcel->getActiveSheet()->setCellValue('D'.$row, 'Rekening Perkiraan Pendapatan ZIS Tidak ada atau bukan data Cabang Anda');
			$row++;
		}else{
			$jumTotl=0;
			foreach ($rsZIS as $detil){
				$strJumlah="SELECT ifnull(SUM(jumlah),0) jmlzis from ak_jurnal WHERE sumber_data IN ('tsetoranzis','tinsidentil')  AND jenis='BKM' AND idperkkredit ='". $detil->idacc."' and status_validasi=1 and bulan='$bln' and tahun='$thn' and  ( isjurnalbalik='off' or isjurnalbalik='0')";
				$rsJmlZIS=$this->db->query($strJumlah)->row();
				
