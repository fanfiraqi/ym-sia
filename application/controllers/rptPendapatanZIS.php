		$objPHPExcel->getActiveSheet()->setCellValue('F'.$row, "Rp. ".number_format($harusSetor,2,',','.'))->getStyle('F'.$row)->getFont()->setBold(true);
		$objPHPExcel->getActiveSheet()->getStyle('F'.$row)->getFont()->setSize(14);

		$row+=3;		
		$objPHPExcel->getActiveSheet()->setCellValue('B'.$row, strtoupper($rsCab->KOTA).", ".strftime('%d %B %Y'));
		$row+=2;
