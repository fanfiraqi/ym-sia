	public function index()
	{
		//$akunKas=$this->divTreeKas($this->common_model->comboGetrptPendapatanZISAll()->result_array(), '1-1100');
		$this->template->set('breadcrumbs','<li><a href="#">Laporan</a></li><li><a href="#">Pendapatan Penghimpunan</a></li>');
		$this->template->set('pagetitle','Filter Laporan Pendapatan ZIS');		
		//$data['akunKas'] = $akunKas;
